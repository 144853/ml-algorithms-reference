"""
Deep Isolation Forest - PyTorch Implementation
===============================================

Theory & Mathematics:
    This module implements a Deep Isolation Forest (Deep IF) using PyTorch.
    While the classical Isolation Forest uses random axis-parallel splits,
    this neural approach learns a differentiable anomaly scoring function
    that can capture non-linear relationships and complex anomaly patterns.

    Architecture -- Neural Anomaly Scorer:
        The model consists of a deep network that learns to predict how
        "isolatable" each sample is. The key ideas are:

        1. Random Network Distillation (RND) Approach:
           - A fixed random "target" network T(x) produces random embeddings
           - A trainable "predictor" network P(x) tries to match T(x)
           - Normal samples (seen during training) yield low prediction error
           - Anomalies (unseen patterns) yield high prediction error
           - Anomaly score = ||P(x) - T(x)||^2

        2. Deep Isolation Forest Representation:
           - Train an ensemble of neural networks, each performing random
             non-linear projections of the data
           - Compute isolation-based path lengths in the learned representation
             space rather than the original feature space
           - This allows capturing non-axis-aligned anomaly boundaries

        3. Differentiable Anomaly Score:
           The anomaly score is computed via a neural network as:
               score(x) = sigma( f_theta(x) )
           where f_theta is a learned function and sigma is a sigmoid activation.
           The network is trained with a contrastive-style loss:
               L = -E[log(1 - score(x_normal))] - E[log(score(x_pseudo_anomaly))]
           Pseudo-anomalies are generated by uniform sampling in the feature
           space bounding box, providing negative examples.

    This Implementation:
        We implement the Random Network Distillation (RND) variant, which is
        elegant, effective, and truly differentiable. The predictor network
        is trained to match a frozen random target network on normal data.
        At inference, high reconstruction error signals anomalies.

Business Use Cases:
    - Complex fraud detection where anomalies have non-linear boundaries
    - High-dimensional anomaly detection (images, embeddings)
    - Streaming anomaly detection with online learning capability
    - Anomaly detection in learned representations (e.g., after a pretrained encoder)

Advantages:
    - Captures non-linear and non-axis-aligned anomaly patterns
    - Differentiable: can be integrated into larger deep learning pipelines
    - GPU acceleration for large-scale data
    - Can be fine-tuned with gradient-based optimization
    - RND approach is simple yet effective

Disadvantages:
    - Requires more computation than classical Isolation Forest
    - Needs hyperparameter tuning (learning rate, architecture)
    - Risk of overfitting on small datasets
    - Less interpretable than tree-based methods
    - Training requires specifying number of epochs

Key Hyperparameters:
    - hidden_dims    : list of hidden layer dimensions
    - embedding_dim  : dimension of target/predictor output embeddings
    - learning_rate  : optimizer learning rate
    - n_epochs       : number of training epochs
    - batch_size     : mini-batch size
    - contamination  : expected proportion of anomalies (for threshold)
    - weight_decay   : L2 regularization

References:
    - Burda, Y., Edwards, H., Storkey, A. and Klimov, O., 2019.
      Exploration by random network distillation. ICLR 2019.
    - Xu, H., Pang, G., Wang, Y. and Wang, Y., 2023.
      Deep isolation forest for anomaly detection. IEEE TKDE.
"""

import warnings
from typing import Any, Dict, List, Optional, Tuple

import numpy as np
import optuna
import torch
import torch.nn as nn
import torch.optim as optim
from sklearn.metrics import (
    average_precision_score,
    confusion_matrix,
    f1_score,
    precision_recall_fscore_support,
    roc_auc_score,
)
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler
from torch.utils.data import DataLoader, TensorDataset

warnings.filterwarnings("ignore")

DEVICE = torch.device("cuda" if torch.cuda.is_available() else "cpu")


# ---------------------------------------------------------------------------
# Neural Network Modules
# ---------------------------------------------------------------------------


class TargetNetwork(nn.Module):
    """
    Fixed random target network for RND-based anomaly detection.

    This network's weights are frozen (never trained). It produces random
    but deterministic embeddings for any input.
    """

    def __init__(
        self,
        input_dim: int,
        hidden_dims: List[int],
        embedding_dim: int,
    ) -> None:
        super().__init__()
        layers: List[nn.Module] = []
        prev_dim = input_dim
        for h_dim in hidden_dims:
            layers.append(nn.Linear(prev_dim, h_dim))
            layers.append(nn.ReLU())
            prev_dim = h_dim
        layers.append(nn.Linear(prev_dim, embedding_dim))
        self.net = nn.Sequential(*layers)

        # Freeze all parameters
        for param in self.parameters():
            param.requires_grad = False

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self.net(x)


class PredictorNetwork(nn.Module):
    """
    Trainable predictor network for RND-based anomaly detection.

    Trained to match the output of the target network on normal data.
    High prediction error indicates anomaly.
    """

    def __init__(
        self,
        input_dim: int,
        hidden_dims: List[int],
        embedding_dim: int,
    ) -> None:
        super().__init__()
        layers: List[nn.Module] = []
        prev_dim = input_dim
        for h_dim in hidden_dims:
            layers.append(nn.Linear(prev_dim, h_dim))
            layers.append(nn.ReLU())
            layers.append(nn.BatchNorm1d(h_dim))
            layers.append(nn.Dropout(0.1))
            prev_dim = h_dim
        layers.append(nn.Linear(prev_dim, embedding_dim))
        self.net = nn.Sequential(*layers)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        return self.net(x)


class DeepIsolationForest:
    """
    Deep Isolation Forest using Random Network Distillation (RND).

    The model trains a predictor network to match a frozen random target
    network on normal data. Anomalies produce high prediction error because
    the predictor has never seen similar patterns during training.

    Parameters
    ----------
    input_dim : int
        Number of input features.
    hidden_dims : list of int
        Hidden layer sizes for both networks.
    embedding_dim : int
        Output embedding dimension.
    learning_rate : float
        Optimizer learning rate.
    weight_decay : float
        L2 regularization coefficient.
    n_epochs : int
        Number of training epochs.
    batch_size : int
        Mini-batch size.
    contamination : float
        Expected fraction of anomalies (for threshold).
    random_state : int
        Random seed.
    """

    def __init__(
        self,
        input_dim: int = 10,
        hidden_dims: Optional[List[int]] = None,
        embedding_dim: int = 32,
        learning_rate: float = 1e-3,
        weight_decay: float = 1e-5,
        n_epochs: int = 50,
        batch_size: int = 64,
        contamination: float = 0.05,
        random_state: int = 42,
    ) -> None:
        self.input_dim = input_dim
        self.hidden_dims = hidden_dims or [64, 32]
        self.embedding_dim = embedding_dim
        self.learning_rate = learning_rate
        self.weight_decay = weight_decay
        self.n_epochs = n_epochs
        self.batch_size = batch_size
        self.contamination = contamination
        self.random_state = random_state

        torch.manual_seed(random_state)
        np.random.seed(random_state)

        self.target_net = TargetNetwork(
            input_dim, self.hidden_dims, embedding_dim
        ).to(DEVICE)
        self.predictor_net = PredictorNetwork(
            input_dim, self.hidden_dims, embedding_dim
        ).to(DEVICE)

        self.optimizer = optim.Adam(
            self.predictor_net.parameters(),
            lr=learning_rate,
            weight_decay=weight_decay,
        )
        self.threshold_: float = 0.0
        self.train_losses_: List[float] = []

    def fit(self, X: np.ndarray) -> "DeepIsolationForest":
        """
        Train the predictor to match the target on normal data.

        Parameters
        ----------
        X : np.ndarray of shape (n, d)
            Training data (mostly normal).

        Returns
        -------
        self
        """
        X_tensor = torch.FloatTensor(X).to(DEVICE)
        dataset = TensorDataset(X_tensor)
        loader = DataLoader(dataset, batch_size=self.batch_size, shuffle=True, drop_last=False)

        self.predictor_net.train()
        self.target_net.eval()

        for epoch in range(self.n_epochs):
            epoch_loss = 0.0
            n_batches = 0

            for (batch_x,) in loader:
                self.optimizer.zero_grad()

                with torch.no_grad():
                    target_emb = self.target_net(batch_x)
                pred_emb = self.predictor_net(batch_x)

                # MSE loss between predictor and target embeddings
                loss = torch.mean(torch.sum((pred_emb - target_emb) ** 2, dim=1))
                loss.backward()
                self.optimizer.step()

                epoch_loss += loss.item()
                n_batches += 1

            avg_loss = epoch_loss / max(n_batches, 1)
            self.train_losses_.append(avg_loss)

        # Set threshold based on training data scores
        scores = self.anomaly_score(X)
        self.threshold_ = float(np.percentile(scores, 100 * (1 - self.contamination)))

        return self

    @torch.no_grad()
    def anomaly_score(self, X: np.ndarray) -> np.ndarray:
        """
        Compute anomaly scores (higher = more anomalous).

        Score = ||P(x) - T(x)||^2 averaged over embedding dimensions.

        Parameters
        ----------
        X : np.ndarray of shape (n, d)

        Returns
        -------
        np.ndarray of shape (n,)
        """
        self.predictor_net.eval()
        self.target_net.eval()

        X_tensor = torch.FloatTensor(X).to(DEVICE)
        dataset = TensorDataset(X_tensor)
        loader = DataLoader(dataset, batch_size=self.batch_size, shuffle=False)

        all_scores = []
        for (batch_x,) in loader:
            target_emb = self.target_net(batch_x)
            pred_emb = self.predictor_net(batch_x)
            mse = torch.mean((pred_emb - target_emb) ** 2, dim=1)
            all_scores.append(mse.cpu().numpy())

        return np.concatenate(all_scores)

    def predict(self, X: np.ndarray) -> np.ndarray:
        """
        Predict anomaly labels.

        Returns
        -------
        np.ndarray of shape (n,)
            1 = anomaly, 0 = normal.
        """
        scores = self.anomaly_score(X)
        return (scores >= self.threshold_).astype(int)

    def decision_function(self, X: np.ndarray) -> np.ndarray:
        """Return raw anomaly scores."""
        return self.anomaly_score(X)


