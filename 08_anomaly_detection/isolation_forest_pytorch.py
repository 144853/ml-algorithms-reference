def optuna_objective(
    trial: optuna.Trial,
    X_train: np.ndarray,
    X_val: np.ndarray,
    y_val: np.ndarray,
) -> float:
    """
    Optuna objective for Deep Isolation Forest hyperparameter search.

    Maximizes validation F1-score.
    """
    n_layers = trial.suggest_int("n_layers", 1, 3)
    hidden_dims = []
    for i in range(n_layers):
        dim = trial.suggest_categorical(f"hidden_dim_{i}", [32, 64, 128, 256])
        hidden_dims.append(dim)

    embedding_dim = trial.suggest_categorical("embedding_dim", [16, 32, 64])
    learning_rate = trial.suggest_float("learning_rate", 1e-4, 1e-2, log=True)
    weight_decay = trial.suggest_float("weight_decay", 1e-6, 1e-3, log=True)
    n_epochs = trial.suggest_int("n_epochs", 20, 100)
    batch_size = trial.suggest_categorical("batch_size", [32, 64, 128])
    contamination = trial.suggest_float("contamination", 0.01, 0.15)

    model = train(
        X_train,
        hidden_dims=hidden_dims,
        embedding_dim=embedding_dim,
        learning_rate=learning_rate,
        weight_decay=weight_decay,
        n_epochs=n_epochs,
        batch_size=batch_size,
        contamination=contamination,
        random_state=42,
    )

    metrics = validate(model, X_val, y_val)
    return metrics["f1"]


def ray_tune_search(
    X_train: np.ndarray,
    X_val: np.ndarray,
    y_val: np.ndarray,
    num_samples: int = 20,
) -> Dict[str, Any]:
    """
    Hyperparameter search using Ray Tune with Optuna.

    Parameters
    ----------
    X_train, X_val, y_val : np.ndarray
        Training/validation data.
    num_samples : int
        Number of trials.

    Returns
    -------
    dict
        Best config and metrics.
    """
    import ray
    from ray import tune
    from ray.tune.search.optuna import OptunaSearch

    ray.init(ignore_reinit_error=True, log_to_driver=False)

    def trainable(config: Dict[str, Any]) -> None:
        hidden_dims = config["hidden_dims"]
        model = train(
            X_train,
            hidden_dims=hidden_dims,
            embedding_dim=config["embedding_dim"],
            learning_rate=config["learning_rate"],
            weight_decay=config["weight_decay"],
            n_epochs=config["n_epochs"],
            batch_size=config["batch_size"],
            contamination=config["contamination"],
            random_state=42,
        )
        metrics = validate(model, X_val, y_val)
        tune.report(
            f1=metrics["f1"],
            roc_auc=metrics["roc_auc"],
            precision=metrics["precision"],
            recall=metrics["recall"],
        )

    search_space = {
        "hidden_dims": tune.choice([[32, 16], [64, 32], [128, 64], [64, 32, 16]]),
        "embedding_dim": tune.choice([16, 32, 64]),
        "learning_rate": tune.loguniform(1e-4, 1e-2),
        "weight_decay": tune.loguniform(1e-6, 1e-3),
        "n_epochs": tune.randint(20, 100),
        "batch_size": tune.choice([32, 64, 128]),
        "contamination": tune.uniform(0.01, 0.15),
    }

    optuna_search = OptunaSearch(metric="f1", mode="max")

    tuner = tune.Tuner(
        trainable,
        param_space=search_space,
        tune_config=tune.TuneConfig(
            search_alg=optuna_search,
            num_samples=num_samples,
            metric="f1",
            mode="max",
        ),
        run_config=ray.train.RunConfig(verbose=0),
    )

    results = tuner.fit()
    best_result = results.get_best_result(metric="f1", mode="max")
    best_config = best_result.config
    best_metrics = best_result.metrics

    ray.shutdown()

    return {
        "best_config": best_config,
        "best_f1": best_metrics.get("f1"),
        "best_roc_auc": best_metrics.get("roc_auc"),
    }


# ---------------------------------------------------------------------------
# Main Pipeline
# ---------------------------------------------------------------------------


def main() -> None:
    """Run the complete Deep Isolation Forest pipeline."""
    print("=" * 70)
    print("Deep Isolation Forest (RND) - PyTorch Implementation")
    print("=" * 70)

    # 1. Generate data
    print("\n[1/5] Generating synthetic anomaly detection data...")
    X_train, X_val, X_test, y_train, y_val, y_test = generate_data(
        n_samples=2000, n_features=10, contamination=0.05, random_state=42
    )

    # 2. Train
    print("\n[2/5] Training Deep Isolation Forest (RND)...")
    model = train(
        X_train,
        hidden_dims=[64, 32],
        embedding_dim=32,
        learning_rate=1e-3,
        n_epochs=50,
        batch_size=64,
        contamination=0.05,
    )
    print(f"  Final training loss: {model.train_losses_[-1]:.6f}")

    # 3. Validate
    print("\n[3/5] Validation results:")
    val_metrics = validate(model, X_val, y_val)
    for k, v in val_metrics.items():
        if k != "confusion_matrix":
            print(f"  {k:>20s}: {v:.4f}")
    print(f"  Confusion Matrix:\n{val_metrics['confusion_matrix']}")

    # 4. Optuna
    print("\n[4/5] Running Optuna hyperparameter optimization (15 trials)...")
    study = optuna.create_study(direction="maximize")
    study.optimize(
        lambda trial: optuna_objective(trial, X_train, X_val, y_val),
        n_trials=15,
        show_progress_bar=False,
    )
    print(f"  Best trial F1: {study.best_value:.4f}")
    print(f"  Best params: {study.best_params}")

    # Retrain with best params
    best_p = study.best_params.copy()
    n_layers = best_p.pop("n_layers")
    hidden_dims = [best_p.pop(f"hidden_dim_{i}") for i in range(n_layers)]
    best_model = train(X_train, hidden_dims=hidden_dims, **best_p, random_state=42)

    # 5. Test
    print("\n[5/5] Test results (best model):")
    test_metrics = test(best_model, X_test, y_test)
    for k, v in test_metrics.items():
        if k != "confusion_matrix":
            print(f"  {k:>20s}: {v:.4f}")
    print(f"  Confusion Matrix:\n{test_metrics['confusion_matrix']}")

    print("\n" + "=" * 70)
    print("Pipeline complete.")
    print("=" * 70)


if __name__ == "__main__":
    main()
